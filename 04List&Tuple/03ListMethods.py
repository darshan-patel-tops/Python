# List Methods

numbers = [1,4,4,7,9,2,46,57,234]

# Will Sort the list
numbers.sort()
print(numbers)

# Will Reverse the list
numbers.reverse()
print(numbers)

# Will Append the list adds at the end of the list
numbers.append(19)
print(numbers)

# Will Insert at the desired Index
numbers.insert(1,577) # Here 1 is index and 577 is the value that will be added at 1 index
print(numbers)

# Will Pop/Remove the value at specified index
numbers.pop(1)
print(numbers)

# Will remove that value from the List but only one time it wont remove multiple value of same name
numbers.remove(4)
print(numbers)

